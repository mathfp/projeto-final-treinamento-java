    private void jtPedidosFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtPedidosFocusGained
        int selecionado = jtClientes.getSelectedRow();
        if (selecionado >= 0) {
            String idString = (String) jtClientes.getValueAt(
                    selecionado, 0);
            jtPedidos.setModel(RestauranteFacade.
                    gerarModeloTabelaPedidos(idString));
        } else {
            jtpAbas.setSelectedIndex(0);
        }
    }//GEN-LAST:event_jtPedidosFocusGained

    private void setarCamposPedidos() {
        if (jtPedidos.getSelectedRow() != -1) {
            int linhaDoPedido = jtPedidos.getSelectedRow();
            Pedido pedido = getPedidoPorLinha(linhaDoPedido);
            jtfValor1.setText(pedido.getValor());
            jcbComida1.setSelectedItem(pedido.getPrato().getTipoPrato());
            jcbBebida1.setSelectedItem(pedido.getBedida().getBebida());
        }
    }


