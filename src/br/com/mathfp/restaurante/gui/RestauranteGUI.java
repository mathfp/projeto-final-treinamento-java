    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        if (jtPedidos.getSelectedRow() != -1) {
            int linhaDoPedido = jtPedidos.getSelectedRow();
            int id = Integer.parseInt((String) jtPedidos.
                    getValueAt(linhaDoPedido, 0));
            int idCliente = Integer.parseInt((String) jtPedidos.
                getValueAt(linhaDoPedido, 1));
            String prato = (String) jtPedidos.getValueAt(linhaDoPedido, 2);
            String bebida = (String) jtPedidos.getValueAt(linhaDoPedido, 3);
            String valor = (String) jtPedidos.getValueAt(linhaDoPedido, 4);
            String valorRetornado = jtfValor1.getText();
            String pratoRetornado = jcbComida1.getSelectedItem().toString();
            String bebidaRetornado = jcbBebida1.getSelectedItem().toString();
            RestauranteFacade.operacaoAtualizarPedido(
                    id, idCliente, prato, bebida,
                    valor, valorRetornado, pratoRetornado, bebidaRetornado);
            operacaoAtualizarInterfacePedidos(idCliente);
            jtfValor1.setText("");
            jcbComida1.setSelectedIndex(5);
            jcbBebida1.setSelectedIndex(4);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

