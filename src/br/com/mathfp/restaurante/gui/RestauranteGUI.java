    private Pedido getPedidoPorLinha(int linha) {
        int id = Integer.parseInt((String) jtPedidos.getValueAt(linha, 0));
        int idCliente = Integer.parseInt((String) jtPedidos.
                getValueAt(linha, 1));
        String prato = (String) jtPedidos.getValueAt(linha, 2);
        String bebida = (String) jtPedidos.getValueAt(linha, 3);
        String valor = (String) jtPedidos.getValueAt(linha, 4);
        Pedido pedido = RestauranteFacade.
                operacaoRetornarPedido(id, idCliente, prato, bebida, valor);
        return pedido;
    }

    private void operacaoAtualizarInterfacePedidos(int idCliente) {
        jtPedidos.setModel(RestauranteFacade.
                gerarModeloAtualizarTabelaPedidos(idCliente));
        setarCamposPedidos();
    }

    private void operacaoAtualizarInterface() {
        jtClientes.setModel(RestauranteFacade.
                operacaoAtualizarInterfaceCliente());
    }

    private void popularCombos() {
        jcbComida.removeAllItems();
        for (TipoPrato tp : TipoPrato.values()) {
            jcbComida.addItem(tp.getTipoPrato());
        }
        jcbBebida.removeAllItems();
        for (Bebida bebida : Bebida.values()) {
            jcbBebida.addItem(bebida.getBebida());
        }

        jcbComida1.removeAllItems();
        for (TipoPrato tp : TipoPrato.values()) {
            jcbComida1.addItem(tp.getTipoPrato());
        }
        jcbBebida1.removeAllItems();
        for (Bebida bebida : Bebida.values()) {
            jcbBebida1.addItem(bebida.getBebida());
        }
    }

